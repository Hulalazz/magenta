"""Tests for AccompanimentRnnEncoderDecoder."""

# internal imports
import tensorflow as tf

import magenta
from magenta.models.accompaniment_rnn import accompaniment_rnn_encoder_decoder

NOTE_OFF = magenta.music.MELODY_NOTE_OFF
NO_EVENT = magenta.music.MELODY_NO_EVENT


class AccompanimentRnnEncoderDecoderTest(tf.test.TestCase):

  def setUp(self):
    melody_1 = magenta.music.Melody([0, 1, NO_EVENT, 2, NOTE_OFF, 3])
    melody_2 = magenta.music.Melody([3, 2, NO_EVENT, 1, NOTE_OFF, 0])

    self.assertEquals(melody_1.get_major_key(), melody_2.get_major_key())
    self.melody_encoder_decoder = magenta.music.OneHotMelodyEncoderDecoder(
        0, 12, melody_1.get_major_key())

    self.melody_pair = accompaniment_rnn_encoder_decoder.MelodyPair(melody_1,
                                                                    melody_2)

  def testInitValues(self):
    encoder_decoder = (
        accompaniment_rnn_encoder_decoder.AccompanimentRnnEncoderDecoder(
            self.melody_encoder_decoder))
    self.assertEqual(encoder_decoder.min_note, 0)
    self.assertEqual(encoder_decoder.max_note, 12)
    self.assertEqual(encoder_decoder.transpose_to_key,
                     self.melody_pair[0].get_major_key())
    self.assertEqual(encoder_decoder.input_size, 28)
    self.assertEqual(encoder_decoder.num_classes, 14)

